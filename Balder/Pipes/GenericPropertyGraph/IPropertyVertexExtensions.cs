            return (from   Key
                    in     PropertyKeys
                    select new KeyValuePair<TKeyVertex, TValueVertex>(Key, IPropertyVertex.GetProperty(Key))).

                    ToLookup(kvp => kvp.Key, kvp => kvp.Value);

        }

